async function validateOutput(output) {
  const results = {
    passed: [],
    failed: [],
    warnings: []
  };

  console.log('\nðŸ“Š VALIDATION RESULTS\n' + '='.repeat(60));

  // Check for required data points
  console.log('\nâœ“ Checking for required data preservation...');
  for (const dataPoint of REQUIRED_DATA_POINTS) {
    if (output.includes(dataPoint)) {
      results.passed.push(`âœ“ Found: "${dataPoint}"`);
      console.log(`  âœ“ ${dataPoint}`);
    } else {
      results.failed.push(`âœ— Missing: "${dataPoint}"`);
      console.log(`  âœ— MISSING: ${dataPoint}`);
    }
  }

  // Check for forbidden phrases (hallucinations)
  console.log('\nâœ“ Checking for hallucinations/generalizations...');
  for (const pattern of FORBIDDEN_PHRASES) {
    const match = output.match(pattern);
    if (match) {
      results.warnings.push(`âš  Found potentially problematic phrase: "${match[0]}"`);
      console.log(`  âš  WARNING: "${match[0]}"`);
    } else {
      results.passed.push(`âœ“ Clean: No match for ${pattern}`);
    }
  }

  // Check sentence count preservation
  console.log('\nâœ“ Checking for sentence count preservation...');
  const concernSentences = TEST_CONCERNS.reduce((total, concern) => {
    return total + (concern.body.match(/\./g) || []).length;
  }, 0);
  const outputSentences = (output.match(/\./g) || []).length;
  const sentenceDiff = Math.abs(outputSentences - concernSentences);
  
  if (sentenceDiff <= TEST_CONCERNS.length + 5) {
    // Allow Â±1 sentence per concern plus 5 for intro/conclusion
    results.passed.push(`âœ“ Sentence count reasonable: ${outputSentences} (source: ${concernSentences})`);
    console.log(`  âœ“ Sentence count: ${outputSentences} (source had ${concernSentences})`);
  } else {
    results.warnings.push(`âš  Sentence count may indicate summarization: ${outputSentences} vs source ${concernSentences}`);
    console.log(`  âš  Sentence count: ${outputSentences} (source had ${concernSentences}) - may be summarized`);
  }

  // Check word count
  const wordCount = output.split(/\s+/).length;
  console.log('\nâœ“ Checking word count utilization...');
  console.log(`  Word count: ${wordCount} / 2500 (${Math.round(wordCount/2500*100)}%)`);
  
  if (wordCount < 500) {
    results.warnings.push(`âš  Word count very low (${wordCount}) - likely over-summarized`);
  } else if (wordCount < 1000) {
    results.warnings.push(`âš  Word count low (${wordCount}) - may not be comprehensive`);
  } else {
    results.passed.push(`âœ“ Word count adequate: ${wordCount}`);
  }

  // Check for all concerns present
  console.log('\nâœ“ Checking all concerns are represented...');
  for (const concern of TEST_CONCERNS) {
    // Extract first few words as identifier
    const identifier = concern.body.split(' ').slice(0, 5).join(' ');
    if (output.toLowerCase().includes(identifier.toLowerCase())) {
      results.passed.push(`âœ“ Concern present: ${concern.key}`);
      console.log(`  âœ“ ${concern.key}`);
    } else {
      results.failed.push(`âœ— Concern possibly missing: ${concern.key}`);
      console.log(`  âœ— MISSING: ${concern.key}`);
    }
  }

  return results;
}

